# Get input for the base and height of a triangle and calculate its area.

# Then show the result in the console.

# area = (base * height) / 2
 

### a) Without using input()
base = 10
height = 5

area = (base * height) / 2

print("The triangle's area is:", area)


### b) Using input()
# Setting base and height values by taking the user's input
base = float(input("Enter the size for the triangle's base:\n"))
height = float(input("Enter the height for the triangle's base:\n"))

area = (base * height) / 2

area = (base * height) / 2
print(f"The triangle's area is: {area}")
